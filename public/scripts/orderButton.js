function openPlatforms() {

	var orderBtnOverlay = document.getElementById('order-btn-overlay');

	//When the user clicks on the "Order Online" button display overlay
	orderBtnOverlay.style.display = "block";
}

function closePlatforms() {
	
	var orderBtnOverlay = document.getElementById('order-btn-overlay');

	//When user clicks on close button
	orderBtnOverlay.style.display = "none";

}
